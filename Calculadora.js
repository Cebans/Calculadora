class Calculadora {
    sumar(num1, num2) {
        return parseFloat(num1) + parseFloat(num2);
    }

    resta(num1, num2) {
        return parseFloat(num1) - parseFloat(num2);
    }

    multiplicar(num1, num2) {
        return parseFloat(num1) * parseFloat(num2);
    }

    dividir (num1, num2) {
        return parseFloat(num1) / parseFloat(num2);
    }
}